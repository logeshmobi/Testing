package com.mobiversa.payment.dto;

import java.util.List;


public class ListData {
	
	
	private String ag_id;
	private String name;
	
	private List<Amount> amt;
	
	public String getName() {
		return name;
	}
	public String getAg_id() {
		return ag_id;
	}
	public void setAg_id(String ag_id) {
		this.ag_id = ag_id;
	}
	public List<Amount> getAmt() {
		return amt;
	}
	public void setAmt(List<Amount> amt) {
		this.amt = amt;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}

